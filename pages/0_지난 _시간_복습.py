import streamlit as st

# =========================================================================
# 1. 페이지 레이아웃 및 기본 설정
# =========================================================================
st.set_page_config(layout="wide", page_title="공통수학1 - 지난 시간 복습")

# =========================================================================
# 2. 메인 타이틀 및 지난 시간 복습 내용
# =========================================================================
st.title("📝 지난 시간에 무엇을 배웠나요?")
st.write("---")

col1, col2 = st.columns(2)

with col1:
    st.subheader("1. 행렬(Matrix)의 뜻과 성분")
    
    # 교과서의 정확한 정의 문장 반영
    st.markdown("""
    * **행렬:** 여러 개의 수 또는 문자를 직사각형 모양으로 배열하여 괄호로 묶어 놓은 것
    * **성분:** 행렬을 구성하고 있는 각각의 수 또는 문자
    * **행(Row):** 행렬에서 성분을 **가로**로 배열한 줄 (1행, 2행, ...)
    * **열(Column):** 행렬에서 성분을 **세로**로 배열한 줄 (1열, 2열, ...)
    """)
    
    # 2x2 행렬 기본 구조 예시
    st.write(r"$$ A = \begin{pmatrix} a_{11} & a_{12} \\ a_{21} & a_{22} \end{pmatrix} $$")
    st.caption("💡 tip: $a_{ij}$는 $i$행 $j$열에 있는 성분을 의미합니다.")

with col2:
    st.subheader("2. 행렬의 덧셈, 뺄셈, 실수배")
    
    st.markdown("""
    * **덧셈과 뺄셈:** **크기가 같은** 두 행렬에서 **대응하는 성분끼리** 더하거나 뺌
    * **실수배 ($kA$):** 행렬 $A$의 **모든 성분에 공평하게** 실수 $k$를 곱함
    """)
    
    # 실수배 예시 수식
    st.write(r"$$ 2 \cdot \begin{pmatrix} 1 & 2 \\ 3 & 4 \end{pmatrix} = \begin{pmatrix} 2 & 4 \\ 6 & 8 \end{pmatrix} $$")
    st.success("🎯 **복습 완료! 이제 오늘 수업의 핵심인 행렬의 곱셈으로 넘어갈 준비가 되었습니다.")
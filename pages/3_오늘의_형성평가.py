import streamlit as st

# =========================================================================
# 1. 페이지 레이아웃 및 기본 설정
# =========================================================================
st.set_page_config(layout="wide", page_title="공통수학1 - 오늘의 형성 평가")

# =========================================================================
# 2. 메인 타이틀 및 문제 출제
# =========================================================================
st.title("🎯 오늘의 형성 평가 퀴즈")
st.write("---")

st.markdown("#### 📝 [문제] 두 행렬 $A$와 $B$의 크기가 다음과 같을 때, 옳은 설명을 고르세요.")
st.write("")

# 학생들이 크기를 더 쉽게 비교할 수 있도록 박스 형태로 시각화
col_info1, col_info2 = st.columns(2)
with col_info1:
    st.info("📂 **행렬 $A$의 크기:** $3 \\times \\mathbf{2}$ (3행 2열)")
with col_info2:
    st.info("📂 **행렬 $B$의 크기:** $\\mathbf{2} \\times 4$ (2행 4열)")

st.write("")

# =========================================================================
# 3. 퀴즈 선택지 구문
# =========================================================================
quiz_ans = st.radio(
    "정답이라고 생각하는 보기를 선택하세요:", 
    [
        "1. A × B는 앞의 열(2)과 뒤의 행(2)의 개수가 달라서 계산할 수 없다.",
        "2. A × B는 계산이 가능하며, 순서를 바꾼 B × A도 항상 같은 결과가 나온다.",
        "3. A × B는 계산이 가능하지만, 순서를 바꾼 B × A는 크기 조건이 맞지 않아 정의되지 않는다. 즉, 교환법칙이 성립하지 않는다."
    ]
)

st.write("")

# =========================================================================
# 4. 정답 확인 및 피드백 패널
# =========================================================================
if st.button("🔮 정답 확인하기", type="primary"):
    if "3." in quiz_ans:
        st.balloons()  # 화면에 폭죽 효과
        st.success("🎉 **정답입니다!** 행렬의 곱셈 연산 조건과 교환법칙이 성립하지 않는 성질을 완벽하게 이해하셨네요!")
    else:
        st.error("❌ **다시 한번 생각해 볼까요?** 앞 행렬의 가로(열)와 뒤 행렬의 세로(행)인 **'안쪽 크기 숫자'**를 차분하게 비교해 보세요!")